def get_worker_name(worker_id: str) -> str:
    """Return the display name for a worker, or the worker_id if not found."""
    entry = WORKER_NAMES.get(worker_id)
    return entry["name"] if entry else worker_id


def get_leader_persona(domain: str) -> dict:
    """Get the persona for a leader domain. Returns empty dict if not found."""
    return LEADER_PERSONAS.get(domain, {})


def get_worker_persona(worker_domain: str) -> dict:
    """Get the persona for a worker domain. Returns empty dict if not found."""
    return WORKER_PERSONAS.get(worker_domain, {})


def register_custom_persona(worker_domain: str, persona: dict) -> None:
    """Register a custom persona for a plugin-defined worker."""
    WORKER_PERSONAS[worker_domain] = persona
