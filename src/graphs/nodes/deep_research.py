def _safe_filename(domain: str) -> str:
    """도메인명을 안전한 파일명으로 변환."""
    safe = re.sub(r'[^\w가-힣-]', '_', domain.strip())[:30]
    return safe.lower() or "unknown"


def _deep_result_to_report(result: DeepResearchResult) -> dict:
    """DeepResearchResult를 export_report용 final_report dict로 변환."""
    findings = []
    for f in result.key_findings:
        if hasattr(f, "model_dump"):
            findings.append(f.model_dump())
        elif isinstance(f, dict):
            findings.append(f)
    return {
        "executive_summary": result.executive_summary,
        "domain_results": [{
            "domain": result.domain,
            "summary": result.executive_summary,
            "quality_score": result.confidence_score,
            "key_deliverables": result.sources[:10] if result.sources else [],
            "gaps": result.gaps if result.gaps else [],
            "file_paths": [],
        }],
        "overall_gap_analysis": ", ".join(result.gaps) if result.gaps else "",
        "recommendations": [],
        "report_html": result.report_html,
    }


def _save_domain_html(result: DeepResearchResult, session_id: str) -> str:
    """result_[domain].html 저장 via export_report. 반환: 저장 경로."""
    from src.utils.report_exporter import export_report
    report_dict = _deep_result_to_report(result)
    domain_session = f"{session_id}/domain_{_safe_filename(result.domain)}"
    output_dir = export_report(
        final_report=report_dict,
        workers=[],
        user_task=result.domain,
        session_id=domain_session,
    )
    if output_dir:
        logger.info("deep_research_html_saved", path=output_dir, domain=result.domain)
        return output_dir
    # Fallback: 직접 저장
    settings = get_settings()
    fallback_dir = Path(settings.report_output_dir) / session_id
    fallback_dir.mkdir(parents=True, exist_ok=True)
    path = fallback_dir / f"result_{_safe_filename(result.domain)}.html"
    full_html = _wrap_full_html(result.report_html, result.domain, result.executive_summary)
    path.write_text(full_html, encoding="utf-8")
    return str(path)


