@node_error_handler("deep_research")
def deep_research_node(state: dict) -> dict:
    """Deep Research 메인 노드.

    도메인 수에 따라 분기:
    - 1개: 단일 실행 → result.html → CEO 스킵, 바로 종료
    - N개: 병렬 실행 → result_[domain].html × N → CEO 통합 리포트로 진행
    """
    selected_domains = state.get("selected_domains", [])
    domains = selected_domains if selected_domains else ["research"]
    user_task = state.get("user_task", "")
    work_order = user_task  # work_order removed — use user_task directly
    session_id = state.get("session_id", "unknown")

    # 명확화 Q&A가 있으면 work_order에 합쳐서 컨텍스트 전달
    questions = state.get("clarifying_questions", [])
    answers = state.get("user_answers", [])
    if questions and answers:
        qa_context = "\n".join(
            f"Q: {q}\nA: {a}" for q, a in zip(questions, answers)
        )
        work_order = f"{work_order}\n\n## 사용자 명확화 답변\n{qa_context}"

    if not domains:
        logger.warning("deep_research_no_domains")
        return {"phase": "error", "error_message": "Deep Research: 도메인이 없습니다."}

    # Initialize progress tracker
    tracker = get_tracker(session_id)
    tracker.start([
        (f"deep_research_{_safe_filename(d)}", d) for d in domains
    ])

    messages: list[AIMessage] = []
    start_time = time.monotonic()

    if len(domains) == 1:
        # ── 단일 도메인: 직접 실행 + result.html + CEO 스킵 ──
        domain = domains[0]
        wid = f"deep_research_{_safe_filename(domain)}"
        tracker.update(wid, WorkerStatus.RUNNING, tier=1)

        logger.info("deep_research_single_start", domain=domain)
        try:
            def _progress_cb(turn: int, max_t: int) -> None:
                if max_t > 0:
                    tracker.set_real_progress(wid, turn / max_t)

            async def _run_and_reflect():
                _start = time.monotonic()
                res = await _run_single_researcher(
                    domain, user_task, work_order, session_id,
                    progress_callback=_progress_cb,
                )
                elapsed = time.monotonic() - _start
                return await _reflect_on_deep_research(
                    res, user_task, domain, work_order, session_id, elapsed,
                )

            result, _refl_status = run_async(_run_and_reflect())
            logger.info("deep_research_reflection", domain=domain, status=_refl_status)
            report_path = _save_single_result_html(result, session_id)
            tracker.update(wid, WorkerStatus.DONE, tier=1, summary=result.executive_summary[:50])

            # Emit research_finding events for UI
            if session_id:
                from src.modes.common import emit_mode_event
                for finding in result.key_findings:
                    emit_mode_event(session_id, {
                        "type": "research_finding",
                        "data": {
                            "sub_query": domain,
                            "finding": finding.content,
                            "source": finding.source or "",
                            "depth": 1,
                        },
                    })

            elapsed = time.monotonic() - start_time
            logger.info(
                "deep_research_single_done",
                domain=domain,
                confidence=result.confidence_score,
                elapsed=round(elapsed, 1),
            )

            messages.append(AIMessage(
                content=(
                    f"[Deep Research] {domain} 완료 — "
                    f"확신도 {result.confidence_score}/10, "
                    f"{len(result.key_findings)}개 핵심 발견, "
                    f"{len(result.sources)}개 출처"
                )
            ))

            final_report = _to_final_report(result, report_path)
            tracker.stop()

            return {
                "final_report": final_report,
                "report_file_path": report_path,
                "messages": messages,
                "phase": "complete",
                "deep_research_results": [result.model_dump()],
            }

        except Exception as e:
            tracker.update(wid, WorkerStatus.FAILED, tier=1, summary=str(e)[:50])
            tracker.stop()
            logger.error("deep_research_single_failed", domain=domain, error=str(e)[:200])
            raise

    else:
        # ── 다중 도메인: 병렬 실행 + result_[domain].html × N ──
        for d in domains:
            wid = f"deep_research_{_safe_filename(d)}"
            tracker.update(wid, WorkerStatus.RUNNING, tier=1)

        logger.info("deep_research_multi_start", domains=domains, count=len(domains))

        results = run_async(
            _run_parallel_researchers(domains, user_task, work_order, session_id, tracker=tracker)
        )

        # 각 도메인 HTML 저장
        report_paths = []
        for result in results:
            try:
                path = _save_domain_html(result, session_id)
                report_paths.append(path)
            except Exception as e:
                logger.warning("deep_research_save_failed", domain=result.domain, error=str(e)[:100])

            wid = f"deep_research_{_safe_filename(result.domain)}"
            if result.confidence_score >= 3:
                tracker.update(wid, WorkerStatus.DONE, tier=1, summary=result.executive_summary[:50])
            else:
                tracker.update(wid, WorkerStatus.FAILED, tier=1, summary="low confidence")

            # Emit research_finding events for UI
            if session_id:
                from src.modes.common import emit_mode_event
                for finding in result.key_findings:
                    emit_mode_event(session_id, {
                        "type": "research_finding",
                        "data": {
                            "sub_query": result.domain,
                            "finding": finding.content,
                            "source": finding.source or "",
                            "depth": 1,
                        },
                    })

        elapsed = time.monotonic() - start_time
        logger.info(
            "deep_research_multi_done",
            count=len(results),
            elapsed=round(elapsed, 1),
        )

        for r in results:
            messages.append(AIMessage(
                content=(
                    f"[Deep Research] {r.domain}: "
                    f"확신도 {r.confidence_score}/10, "
                    f"{len(r.key_findings)}개 발견"
                )
            ))

        tracker.stop()

        # CEO 통합 리포트로 진행
        return {
            "deep_research_results": [r.model_dump() for r in results],
            "messages": messages,
            "phase": "ceo_final_report",
        }
