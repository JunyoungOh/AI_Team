# ── Single researcher execution ──────────────────


async def _run_single_researcher(
    domain: str,
    user_task: str,
    work_order: str,
    session_id: str,
    progress_callback=None,
    time_budget: float | None = None,
) -> DeepResearchResult:
    """단일 도메인 Deep Research 실행.

    max_turns=200, timeout=40분, effort=high.
    능동적으로 WebSearch/WebFetch/Firecrawl을 반복 사용.
    """
    settings = get_settings()
    bridge = _get_bridge()

    effective_timeout = (
        int(time_budget) if time_budget else settings.deep_research_timeout
    )

    system = DEEP_RESEARCH_SYSTEM.format(
        domain=domain,
        user_task=user_task,
        work_order=work_order,
    )

    result = await bridge.structured_query(
        system_prompt=system,
        user_message=f"리서치 주제: {user_task}\n도메인: {domain}",
        output_schema=DeepResearchResult,
        model=settings.deep_research_model,
        allowed_tools=settings.deep_research_tools,
        timeout=effective_timeout,
        max_turns=settings.deep_research_max_turns,
        effort=settings.deep_research_effort,
        max_tokens=65536,
        progress_callback=progress_callback,
    )
    return result


# ── Reflection ────────────────────────────────────


async def _reflect_on_deep_research(
    result: DeepResearchResult,
    user_task: str,
    domain: str,
    work_order: str,
    session_id: str,
    researcher_elapsed: float,
) -> tuple[DeepResearchResult, str]:
    """Evaluate Deep Research result via Haiku. Returns (final_result, status)."""
    from src.models.messages import ReflectionVerdict
    from src.prompts.reflection_prompts import (
        DEEP_RESEARCH_REFLECTION_SYSTEM, DEEP_RESEARCH_REFLECTION_USER,
    )

    settings = get_settings()
    if not settings.enable_deep_research_reflection:
        return result, "skipped"

    # Sanity check
    sanity_fail = (
        result.confidence_score < 3
        or not result.report_html
        or len(result.report_html) < 200
        or not result.key_findings
    )
    if sanity_fail:
        critique = "Sanity check 실패: 리서치 결과가 비어있거나 극히 부족합니다."
        haiku_elapsed = 0.0
    else:
        # Haiku evaluation
        haiku_start = time.monotonic()
        try:
            bridge = _get_bridge()
            findings_text = "\n".join(
                f"- [{f.category}] {f.content[:200]}" for f in result.key_findings[:10]
            )
            user_msg = DEEP_RESEARCH_REFLECTION_USER.format(
                user_task=user_task,
                executive_summary=result.executive_summary[:1000],
                findings_count=len(result.key_findings),
                findings_text=findings_text,
            )
            verdict = await bridge.structured_query(
                system_prompt=DEEP_RESEARCH_REFLECTION_SYSTEM,
                user_message=user_msg,
                output_schema=ReflectionVerdict,
                model=settings.reflection_model,
                allowed_tools=[],
                timeout=settings.reflection_timeout,
            )
            haiku_elapsed = time.monotonic() - haiku_start
        except Exception as e:
            logger.warning("deep_research_reflection_failed", error=str(e)[:100])
            return result, "pass"

        if verdict.passed:
            return result, "pass"
        critique = verdict.critique

    # Time budget check
    repair_budget = settings.deep_research_timeout - researcher_elapsed - haiku_elapsed
    if repair_budget < settings.reflection_min_repair_budget:
        logger.warning("deep_research_reflection_no_budget", budget=round(repair_budget, 1))
        return result, "fail_kept_original"

    # Repair
    logger.info("deep_research_reflection_repair", domain=domain, critique=critique[:100])
    try:
        repair_work_order = (
            f"{work_order}\n\n"
            f"## 이전 리서치 피드백 (반드시 반영)\n{critique}\n"
            f"이전 결과를 참고하되 지적 사항을 보완하세요."
        )
        repair_result = await _run_single_researcher(
            domain, user_task, repair_work_order, session_id,
            time_budget=repair_budget,
        )
    except Exception as e:
        logger.warning("deep_research_repair_failed", error=str(e)[:100])
        return result, "fail_kept_original"

    # Compare
    repair_better = (
        repair_result.confidence_score >= result.confidence_score
        and len(repair_result.key_findings) >= len(result.key_findings)
        and len(repair_result.report_html) >= len(result.report_html) * 0.8
    )
    if repair_better:
        logger.info("deep_research_repair_adopted", domain=domain)
        return repair_result, "fail_repaired"
    else:
        logger.info("deep_research_repair_rejected", domain=domain)
        return result, "fail_kept_original"


# ── Parallel multi-domain execution ──────────────


async def _run_parallel_researchers(
    domains: list[str],
    user_task: str,
    work_order: str,
    session_id: str,
    tracker=None,
) -> list[DeepResearchResult]:
    """다중 도메인 Deep Research 병렬 실행."""
    settings = get_settings()

    def _make_cb(wid: str):
        def _cb(turn: int, max_t: int) -> None:
            if tracker and max_t > 0:
                tracker.set_real_progress(wid, turn / max_t)
        return _cb

    async def _run_one_with_reflection(d, cb):
        _start = time.monotonic()
        res = await _run_single_researcher(
            d, user_task, work_order, session_id,
            progress_callback=cb,
        )
        elapsed = time.monotonic() - _start
        final, _status = await _reflect_on_deep_research(
            res, user_task, d, work_order, session_id, elapsed,
        )
        return final

    coros = [
        _run_one_with_reflection(
            domain, _make_cb(f"deep_research_{_safe_filename(domain)}"),
        )
        for domain in domains
    ]

    # asyncio.gather with return_exceptions to not lose partial results
    raw_results = await asyncio.gather(*coros, return_exceptions=True)

    results: list[DeepResearchResult] = []
    for domain, raw in zip(domains, raw_results):
        if isinstance(raw, Exception):
            logger.error("deep_research_domain_failed", domain=domain, error=str(raw)[:200])
            # Create a minimal fallback result
            results.append(DeepResearchResult(
                domain=domain,
                executive_summary=f"{domain} 도메인 리서치 실패: {str(raw)[:100]}",
                report_html=f'<div style="padding:20px;color:#c62828;">리서치 실행 실패: {html_mod.escape(str(raw)[:200])}</div>',
                confidence_score=1,
                gaps=[f"리서치 실패: {str(raw)[:100]}"],
            ))
        else:
            results.append(raw)

    return results


# ── Result conversion helpers ────────────────────


def _to_final_report(result: DeepResearchResult, report_path: str) -> dict:
    """DeepResearchResult → CEOFinalReport dict (단일 도메인 직결용)."""
    return {
        "executive_summary": result.executive_summary,
        "domain_results": [{
            "domain": result.domain,
            "summary": result.executive_summary,
            "quality_score": result.confidence_score,
            "key_deliverables": [f.content for f in result.key_findings if f.importance >= 4],
            "gaps": result.gaps,
            "file_paths": [report_path],
        }],
        "overall_gap_analysis": (
            "Deep Research 단일 세션으로 심층 분석 완료."
            if result.confidence_score >= 7
            else f"확신도 {result.confidence_score}/10 — 추가 조사 권장."
        ),
        "recommendations": [],
        "report_html": result.report_html,
    }


def _build_summary_for_ceo(results: list[DeepResearchResult]) -> str:
    """다중 도메인 결과를 CEO 입력으로 생성.

    전체 findings를 importance 순으로 정렬하여 전달.
    CEO가 사용자 지시 맥락에 맞춰 최종 취사선택.
    """
    parts = []
    for r in results:
        parts.append(f"## {r.domain}")
        parts.append(f"요약: {r.executive_summary}")
        parts.append(f"확신도: {r.confidence_score}/10")

        sorted_findings = sorted(r.key_findings, key=lambda f: -f.importance)
        if sorted_findings:
            parts.append("\n수집 데이터 (중요도순):")
            for f in sorted_findings:
                source_tag = f" [{f.source}]" if f.source else ""
                parts.append(f"- [★{f.importance}|{f.category}] {f.content}{source_tag}")

        if r.gaps:
            parts.append(f"\n갭: {', '.join(r.gaps)}")

        if r.sources:
            parts.append(f"\n주요 출처: {', '.join(r.sources[:5])}")

        parts.append("")  # blank line
    return "\n".join(parts)


